/*Write a C++ program that displays all possible combinations of 1, 2, and 3.*/

#include<iostream>
using namespace std;

int main()
{
    cout<<"1 2 3"<<endl;
    cout<<"1 3 2"<<endl;
    cout<<"2 1 3"<<endl;
    cout<<"2 3 1"<<endl;
    cout<<"3 1 2"<<endl;
    cout<<"3 2 1"<<endl;
    return 0;
}
